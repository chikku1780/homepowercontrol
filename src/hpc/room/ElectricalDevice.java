package hpc.room;

/**
 *
 * @author sys6003
 */
public class ElectricalDevice {

    private int edId;
    private String edName;
    private int edStatus;

    public int getEdId() {
        return edId;
    }

    public void setEdId(int edId) {
        this.edId = edId;
    }

    public String getEdName() {
        return edName;
    }

    public void setEdName(String edName) {
        this.edName = edName;
    }

    public int getEdStatus() {
        return edStatus;
    }

    public void setEdStatus(int edStatus) {
        this.edStatus = edStatus;
    }

}
